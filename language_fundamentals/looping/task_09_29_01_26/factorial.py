num = int(input("Enter number:")) #4                    #0

i = 1                             # i = 1               i = 1   
fact = 1                          # fact = 1            fact = 1
while(i <= num):                  # 1 <= 4              1 <= 0(F)
    fact = fact * i               # fact = 1 = 1 * 1
    i += 1                        # 2
print("Factorial of",num,":",fact)                      #1